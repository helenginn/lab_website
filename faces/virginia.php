Virginia Apostolopoulou graduated from the University of Crete, Greece, with a Bachelor’s and Master’s degree in Applied and Computational Mathematics. During her PhD, part of the RAMP network, she entered structural biology. She worked on generating testable predictions of conditions for membrane protein crystallisation. She also has a background in time-resolved crystallography. Now she is working in the Protein Machinists lab on a project in collaboration with Nick Pearce (Linköping University, Sweden). Outside work, she stays active and spends time with those close to her. She enjoys the outdoors and boat ride in the canals of Hamburg, when the weather is good.
