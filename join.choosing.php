<p class="tagline">
How to choose a group leader
</p>
<p>
If you're interested in doing a doctorate or taking up a post-doc position, you will be directly or indirectly supervised by a group leader. Poor quality supervision can make this experience miserable, so it is important to choose your supervisor carefully.
</p>
<p class="title">
A good group leader ensures your voice is heard.
</p>
<p>
Communication in science is key: even in the simple case of communicating our scientific results to an audience, writing papers or designing posters. A good group leader will give the lab members the time and attention needed to build their scientific presentation skills. Communication is also important between people, and so they should encourage clear and open communication between group members, and not to rely on unspoken rules or expectations.
</p>
<p>
You will also have your own scientific goals and career plans. A good group leader does not push you towards solely what is best for the group, but finds the common ground between your needs and the needs of the team.
</p>
<p>
Doing collaborative science always involves inter-personal relationships, and sometimes this leads to conflicts that cannot be resolved without the group leader's involvement. A good group leader will listen to your voices and quickly work to find an acceptable resolution.
</p>
<p>
Too often, under-represented groups in science hit invisible and untangible barriers to career progression. A good group leader will support you in antagonistic external work environments, and ensure the group is a safe space to be your authentic self. A good group fosters an inclusive environment, so that every person knows they can contribute to science without personal judgement. Nobody should feel like a representative for their demographic when asking or answering questions.
</p>
<p class="title">
A good group leader has more than one management style to offer.
</p>
<p>
As a doctorate or post-doc, you are working with a direct scientific supervisor. However, each scientist benefits from a tailored management style, and a good group leader should be able to offer a range of styles. Some people prefer to begin with direct supervision, whereas others prefer a hands-off style with broad guidance. Most of the time, people will fall somewhere in between. A good group leader also encourages development of autonomy and a reduction over time for the need of direct supervision, to help lab members transition into their next role.
</p>
<p class="title">
A good group leader expects a healthy work-life balance.
</p>
<p>
A good group leader encourages their lab members to build or maintain a non-scientific identity as a human being. Academic science can involve setbacks and disappointment at times, and it is important to have as many support networks as possible in and out of science. A group leader should have the experience to recognise that more hours does not translate to increased productivity, but can translate to burnout and chronic stress. Maintaining good mental well-being allows us to do our best science. A good group leader will demonstrate these principles by example.
</p>
<p class="title">
A good group leader will facilitate your best science. 
</p>
<p>
You should feel comfortable communicating problems, getting the support that is right for you, and maintain a sustainable work pattern in a well-managed group. Sometimes, straightforward practical barriers prevent people from doing their best work. A good group leader has the responsibility of dismantling these barriers and allowing lab members to do their best science.
</p>
<p>
A good group leader is also invested in your personal development, and will do their best to foster a sound understanding of the scientific method. A lot of science is figuring out which hill to climb next, establishing which experiment brings the biggest benefit for the effort required. They will mentor you to evaluate your own work realistically, to help you make sound decisions on your scientific direction.
</p>
